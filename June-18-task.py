#Program for string slicing Type 2 and Type 1
#String in focus: "go_kubernetes_laptop" and Type 2(String Slicing)
print("String Slicing Type 2")
h = "go_kubernetes_laptop"
print("String provided is: ", h)
print(h[3:10:1])
print(h[3:10:2])
print(h[3:10:3])
print(h[5:-5:1])
print(h[5:-5:2])
print(h[5:-5:5])
print(h[:8:4])
print(h[5::3])
print(h[::2])
print(h[12:5:3])

print("-------------------------------------------------------------")

# Type 1(String Slicing)

print("String Slicing Type 1")

print(h[3:8])
print(h[5:-5])
print(h[-12:-7])
print(h[-10:15])
print(h[:6])
print(h[:-7])
print(h[5:])
print(h[-10:])
print(h[:])
print(h[4:100])
print(h[12:5])

print("-------------------------------------------------------------")

